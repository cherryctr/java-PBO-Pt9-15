    private void lamainapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lamainapActionPerformed
        // Biaya inap set 
        LamaInap = Integer.parseInt(lamainap.getText());
        
        if (coderoom.getSelectedIndex() == 1 ) {
          Diskon = LamaInap * Tarif*2/100;
          JumlahBayar = Tarif - Diskon;
          
        } 
        else if (coderoom.getSelectedIndex() == 2 ) {
          Diskon = LamaInap * Tarif*3/100;
          JumlahBayar = Tarif - Diskon;
        }
        
        else if (coderoom.getSelectedIndex() == 3 ) {
          Diskon = LamaInap * Tarif*4/100;
          JumlahBayar = Tarif - Diskon;
        }
        
        else if (coderoom.getSelectedIndex() == 4 ) {
          Diskon = LamaInap * Tarif*5/100;
          JumlahBayar = Tarif - Diskon;
        }
        
        else if (coderoom.getSelectedIndex() == 5 ) {
          Diskon = LamaInap * Tarif*60/100;
          JumlahBayar = Tarif - Diskon;
        }
        
      
        
        diskon.setText(String.valueOf(Diskon));
        jumlah_bayar.setText(String.valueOf(JumlahBayar));
    }//GEN-LAST:event_lamainapActionPerformed

