    private void lamainapKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lamainapKeyTyped
        // TODO add your handling code here:
        
        String mrx = lamainap.getText();
        int length = mrx.length();
        
        
        var c = evt.getKeyChar();
        
        //fungsi untuk anti character        
        if(!Character.isDigit(c)){
            evt.consume();
        }
        
        if(evt.getKeyChar()>='0') {
           if(length>1){
            lamainap.setEditable(false);
           }else{
            lamainap.setEditable(true);
           }
        }
    
      
    }//GEN-LAST:event_lamainapKeyTyped

